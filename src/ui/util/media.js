import { css } from 'styled-components'

/******************************************************************************/
// DATA
/******************************************************************************/

const MINI = 500

const PHONE = 600

const TABLET = 800

const DESKTOP = 960

/******************************************************************************/
// Main
/******************************************************************************/

const media = (size, strings, ...params) => css`
  @media (max-width: ${size}px) {
    ${css(strings, ...params)}
  }
`

/******************************************************************************/
// Extend
/******************************************************************************/

media.add = (name, size) => {
  media[name] = {
    css: media.bind(null, size)
  }

  return media[name]
}

media.add('mini', MINI)
media.add('phone', PHONE)
media.add('tablet', TABLET)
media.add('desktop', DESKTOP)

/******************************************************************************/
// Exports
/******************************************************************************/

export default media
